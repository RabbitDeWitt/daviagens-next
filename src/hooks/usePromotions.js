import { listaDestinos } from "@/constants/data"
import { useState } from "react"

const usePromotions = () => {
  const destinosOrdenados = listaDestinos.sort((a, b) => a.nome < b.nome ? -1 : 1)

  const [destinos, setDestinos] = useState(listaDestinos)

  const ordemDestino = [...destinos]

  const listarMenorPreco = () => {
    setDestinos(ordemDestino.sort((a, b) => a.valor < b.valor ? -1 : 1))
  }
  const listarMaiorPreco = () => {
    setDestinos(ordemDestino.sort((a, b) => a.valor > b.valor ? -1 : 1))
  }

  const listarMenorDesconto = () => {
    setDestinos(ordemDestino.sort((a, b) => a.desconto < b.desconto ? -1 : a.desconto === b.desconto ? 0 : 1))
  }

  const listarMaiorDesconto = () => {
    setDestinos(ordemDestino.sort((a, b) => a.desconto > b.desconto ? -1 : a.desconto === b.desconto ? 0 : 1))
  }



  return {
    destinos,
    listarMenorDesconto,
    listarMenorPreco,
    listarMaiorDesconto,
    listarMaiorPreco
  }
}

export default usePromotions